export function createTextureFromPixels(
  pixels: number[][],
  palette: Record<number, string>,
  _size: number = 16
): THREE.CanvasTexture {
  const canvas = document.createElement('canvas');
  canvas.width = pixels[0].length;
  canvas.height = pixels.length;
  const ctx = canvas.getContext('2d')!;

  ctx.clearRect(0, 0, canvas.width, canvas.height);

  for (let y = 0; y < pixels.length; y++) {
    for (let x = 0; x < pixels[y].length; x++) {
      const pixel = pixels[y][x];
      if (pixel === 0) continue;

      const color = palette[pixel];
      if (color) {
        ctx.fillStyle = color;
        ctx.fillRect(x, y, 1, 1);
      }
    }
  }

  const texture = new THREE.CanvasTexture(canvas);
  texture.magFilter = THREE.NearestFilter;
  texture.minFilter = THREE.NearestFilter;
  texture.generateMipmaps = false;
  return texture;
}

export function createCharacterTexture(
  palette: CharacterPalette,
  frame: number[][] = CHARACTER_IDLE[0]
): THREE.CanvasTexture {
  const colorMap: Record<number, string> = {
    1: palette.skin[0],
    2: palette.hair[0],
    3: palette.shirt[0],
    4: palette.pants[0],
    5: palette.eyes,
    6: palette.mouth,
    7: palette.shoes[0],
  };

  return createTextureFromPixels(frame, colorMap);
}

export function getRandomPalette(): CharacterPalette {
  return CHARACTER_PALETTES[Math.floor(Math.random() * CHARACTER_PALETTES.length)];
}
